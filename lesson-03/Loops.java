class Loops 
{
    public static void main(String args[])
    {
        // loops: while, do while, for\
        // number of times OR under certain condition

        for(int i = 1; i <= 5; i++)
        {
            System.out.println("DAY" + i);

            for(int j = 1; j <= 9; j++)
            System.out.println(" " + (j + 8) + "-" + (j + 9));
        }


        // System.out.println("Bye" + i);
    }
    
}
